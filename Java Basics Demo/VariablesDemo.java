public class VariablesDemo {
  public static void main(String[] args) {
    int intAge = 20;
    float fltPi = 3.141593f;
    double dblPrecisePi = 3.141592653589793;
    char charGrade = 'A';
    String strName = "Chris";
    boolean boolIsStudent = true;

    System.out.println("Age: " + intAge);
    System.out.println("Float Pi: " + fltPi);
    System.out.println("Double Pi: " + dblPrecisePi);
    System.out.println("Grade: " + charGrade);
    System.out.println("Name: " + strName);
    System.out.println("Is Student: " + boolIsStudent);
  }
}
