public class StringsDemo {
  public static void main(String[] args) {
    String strWord = "Java Programming";

    System.out.println("Length: " + strWord.length());
    System.out.println("Uppercase: " + strWord.toUpperCase());
    System.out.println("Character at index 5: " + strWord.charAt(5));
    System.out.println("Substring (0-4): " + strWord.substring(0, 4));
  }
}
